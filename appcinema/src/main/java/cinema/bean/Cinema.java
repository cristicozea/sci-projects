package cinema.bean;

public class Cinema {
    private int cId;
    private String cinemaLocation;
    private String cinemaPhoneNumber;
    private String cinemaName;
    public int getcId() {
        return cId;
    }
    public void setcId(int cId) {
        this.cId = cId;
    }
    public String getCinemaLocation() {
        return cinemaLocation;
    }
    public void setCinemaLocation(String cinemaLocation) {
        this.cinemaLocation = cinemaLocation;
    }
    public String getCinemaPhoneNumber() {
        return cinemaPhoneNumber;
    }
    public void setCinemaPhoneNumber(String cinemaPhoneNumber) {
        this.cinemaPhoneNumber = cinemaPhoneNumber;
    }
    public String getCinemaName() {
        return cinemaName;
    }
    public void setCinemaName(String cinemaName) {
        this.cinemaName = cinemaName;
    }
}
